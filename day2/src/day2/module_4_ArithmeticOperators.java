package day2;

public class module_4_ArithmeticOperators {

	public static void main(String[] args) {
		
		//標題是算術運算子的英文
		
		int a = 100;
		System.out.println(a+3);	//加法
		System.out.println(a-3);	//減法
		System.out.println(a*3);	//乘法
		System.out.println(a/3);	//除法
		System.out.println(a%3);	//模數 模除
									//求餘數的說法不精確，比較像是表達與對象被數的誤差
		

	}

}
